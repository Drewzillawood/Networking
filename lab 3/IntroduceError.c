/*-----------------------------------------------------------------
-                       Memory Constants
-----------------------------------------------------------------*/
static long M = 2147483647;

/*----------------------------------------------------------------
-                          Procedures
-----------------------------------------------------------------*/
void IntroduceError( char *data, double p )
{
	char *pointer = data;

	while ( *pointer != '\0' )
    {
        if ( ( (double)random() / M ) <= p )
        {
            *pointer = ( *pointer == '0' ) ? '1' : '0';
        }
        pointer++;
	}
}
